//Write a C program to find the square and cube of a given number.

class Q6{
	public static void main(String[] args){
		
		//initialization
		int num = 2;
		
		//declaration
		int square, cube;
		
		//performing operation
		square = num * num;
		cube = num * num * num;
		
		//printing output
		System.out.printf("square of %d is %d",num, square);
		System.out.printf("\ncube of %d is %d",num, cube);
		
	}

}



